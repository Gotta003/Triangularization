int compare_neighbors(const void* a, const void* b) {
	float diff=((Neighbor*)a)->distance-((Neighbor*)b)->distance;
	return (diff>0)-(diff<0);
}

uint8_t classify_knn(int8_t a1, int8_t a2, int8_t a3) {
    // Allochiamo spazio solo per i K vicini più stretti (es. 3 o 5)
    Neighbor best_neighbors[K_VALUE];

    // Inizializziamo i migliori vicini con distanze infinite
    for (int i = 0; i < K_VALUE; i++) {
        best_neighbors[i].distance = 999999.0f;
        best_neighbors[i].zone_id = 0;
    }

    int8_t current_vector[NUM_ANCHORS] = {a1, a2, a3};

    // Scansione lineare del database
    for (int i = 0; i < DATABASE_SIZE; i++) {
        float sum_sq = 0.0f;
        for (int j = 0; j < NUM_ANCHORS; j++) {
            float diff = (float)(current_vector[j] - training_set[i].rssi[j]);
            sum_sq += diff * diff;
        }
        float dist = sqrtf(sum_sq);

        // Se la distanza calcolata è migliore del peggiore dei nostri K migliori vicini
        if (dist < best_neighbors[K_VALUE - 1].distance) {
            // Inserimento ordinato (Insertion Sort in-place)
            int pos = K_VALUE - 1;
            while (pos > 0 && dist < best_neighbors[pos - 1].distance) {
                best_neighbors[pos] = best_neighbors[pos - 1];
                pos--;
            }
            best_neighbors[pos].distance = dist;
            best_neighbors[pos].zone_id = training_set[i].zone_id;
        }
    }

    // Votazione a maggioranza (votes ha dimensione NUM_ZONES + 1 per gestire l'indice 36)
    uint8_t votes[NUM_ZONES + 1] = {0};
    for (int i = 0; i < K_VALUE; i++) {
        uint8_t zid = best_neighbors[i].zone_id;
        if (zid >= 1 && zid <= NUM_ZONES) {
            votes[zid]++;
        }
    }

    // Trova la zona con il punteggio più alto
    uint8_t predicted_zone = 11; // Default di sicurezza (Perimetro morto)
    uint8_t max_votes = 0;
    for (int i = 1; i <= NUM_ZONES; i++) { // Ciclo corretto da 1 a 36
        if (votes[i] > max_votes) {
            max_votes = votes[i];
            predicted_zone = i; // Restituisce l'ID reale (1-36)
        }
    }

    return predicted_zone;
}
